    {
        /// <summary>
        /// Gets or sets the value indicating whether caching is enabled or not.
        /// </summary>
        bool CacheEnabled { get; set; }

        /// <summary>
        /// Clears the cache.
        /// </summary>
        void ClearCache();
        
        /// <summary>
        /// Creates a raw SQL query that is executed directly in the database and returns a collection of entities.
        /// </summary>
